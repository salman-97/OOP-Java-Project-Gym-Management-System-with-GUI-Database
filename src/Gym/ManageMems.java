    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // Add Button
        if(memname.getText().equals("")||memCont.getText().equals("")||memAdd.getText().equals("")||memPlan.getText().equals("")||memHeitxt.getText().equals("")||memWeitxt.getText().equals("")||memAgetxt.getText().equals("")){
            JOptionPane.showMessageDialog(this, "Please Fill Fields");
        }
        else{
            String name = memname.getText();
            String conT = memCont.getText();
            String adrs = memAdd.getText();
            String plan = memPlan.getText();
            String hgt = memHeitxt.getText()+" "+memHeight.getText();
            String wgt = memWeitxt.getText()+" "+memWeight.getText();
            String aG = memAgetxt.getText();
        try{
            pst = con.prepareStatement("insert into addmember(name,mobile,address,plan,height,weight,age)values(?,?,?,?,?,?,?)");
            pst.setString(1, name);
            pst.setString(2, conT);
            pst.setString(3, adrs);
            pst.setString(4, plan);
            pst.setString(5, hgt);
            pst.setString(6, wgt);
            pst.setString(7, aG);
            pst.executeUpdate();
            JOptionPane.showMessageDialog(this, "Record Added");
            memname.requestFocus();
            clear_fields();
        }
        catch (SQLException ex) {
            Logger.getLogger(ManageMems.class.getName()).log(Level.SEVERE, null, ex);
        }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

