    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        
        df = (DefaultTableModel)jTable1.getModel();
        int seleC = jTable1.getSelectedRow();
        
        int id = Integer.parseInt(df.getValueAt(seleC, 0).toString());
        
            String name = memname.getText();
            String conT = memCont.getText();
            String adrs = memAdd.getText();
            String plan = memPlan.getText();
            String hgt = memHeitxt.getText() + " " + memHeight.getText();
            String wgt = memWeitxt.getText() + " " + memWeight.getText();
            String aG = memAgetxt.getText();
        try{
            pst = con.prepareStatement("update addmember set name=?,mobile=?,address=?,plan=?,height=?,weight=?,age=? where id = ?");
            pst.setString(1, name);
            pst.setString(2, conT);
            pst.setString(3, adrs);
            pst.setString(4, plan);
            pst.setString(5, hgt);
            pst.setString(6, wgt);
            pst.setString(7, aG);
            pst.setInt(8, id);
            
            pst.executeUpdate();
            JOptionPane.showMessageDialog(this, "Record Updated");
            memname.requestFocus();
            
            clear_fields();
        }
        catch (SQLException ex) {
            Logger.getLogger(ManageMems.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton3ActionPerformed

